        <h3>Chart Types</h3>
        <div style="display:grid;grid-template-columns:repeat(auto-fit,minmax(280px,1fr));gap:1rem;">
            <div><strong>Bar Charts</strong><br><span style="color:var(--text-secondary);">Compare values across categories or years. Stacked bars show composition. Horizontal bars for rankings.</span></div>
            <div><strong>Line Charts</strong><br><span style="color:var(--text-secondary);">Show trends over time. Multiple lines for comparison. Dashed forecast lines for projections.</span></div>
            <div><strong>Doughnut/Pie Charts</strong><br><span style="color:var(--text-secondary);">Show proportion breakdown. Center text shows total. Click segments to filter.</span></div>
            <div><strong>Scatter Plots</strong><br><span style="color:var(--text-secondary);">Each point = one district or school. Reveals correlations between two metrics.</span></div>
            <div><strong>Radar Charts</strong><br><span style="color:var(--text-secondary);">Multi-metric comparison across districts. Each axis = one metric.</span></div>
            <div><strong>Maps</strong><br><span style="color:var(--text-secondary);">County and town-level choropleth maps. Green=low, red=high. Click to drill down.</span></div>
        </div>
    </div>

    <div class="chart-card" data-animate style="margin-bottom:1.5rem;">
        <h3>Tips</h3>
        <ul style="color:var(--text-secondary);">
            <li><strong>Download charts</strong> — Click the <em>PNG</em> button below any chart to save it as an image.</li>
            <li><strong>Export data</strong> — Use the <em>Download CSV</em> link on data browser pages, or visit <a href="/data/export" style="color:var(--accent-glow);">/data/export</a> for bulk download.</li>
            <li><strong>Filtering</strong> — Use the school year and district dropdowns on data browser pages to narrow results.</li>
            <li><strong>Maps</strong> — Toggle between <em>Discrete Colors</em> (percentile-based) and <em>Smooth Gradient</em> (continuous). Hover for exact values, click to drill down.</li>
            <li><strong>Keyboard navigation</strong> — Tab to any chart and use left/right arrow keys to move through data points.</li>
            <li><strong>Printing</strong> — Use browser print (Ctrl+P) for print-optimized reports. Hidden nav, footer, and buttons.</li>
        </ul>
    </div>

    <div class="chart-card" data-animate>
